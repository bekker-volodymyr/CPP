#include <iostream>
#include <Windows.h>

using namespace std;

// Функція main тепер приймає аргументи командного рядка:
// argc - кількість аргументів (включаючи назву програми)
// args[] - масив покажчиків на рядки (char*), що містять самі аргументи
int main(int argc, char* args[])
{
    // Встановлення кодування консолі для коректного відображення українських символів.
    SetConsoleOutputCP(1251);

    // args[0] завжди містить назву (шлях) до виконуваного файлу програми.
    cout << "Перший параметр (назва програми): " << args[0] << endl;

    // Перевіряємо, чи є додаткові аргументи, окрім назви програми.
    if (argc > 1)
    {
        // Якщо є, проходимося циклом по всіх аргументах і виводимо їх.
        // Індекс 'i' починається з 0, але для зручності відображення додаємо 1.
        for (int i = 0; i < argc; i++) {
            cout << "Параметр " << i + 1 << ": " << args[i] << endl;
        }
    }
    // Якщо argc не більше 1, це означає, що програма була запущена без додаткових аргументів.
    else {
        cout << "Програму запущено без додаткових параметрів." << endl;
    }

    return 0; // Повернення 0 вказує на успішне завершення програми.
}
