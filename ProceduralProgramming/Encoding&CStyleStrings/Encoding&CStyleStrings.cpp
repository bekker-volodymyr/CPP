#include <iostream>  // Для введення/виведення (cout)
#include <Windows.h> // Для функцій роботи з консоллю Windows (SetConsoleOutputCP, SetConsoleCP)

using namespace std;

int main()
{
    // Встановлення кодування консолі для коректного відображення символів.
    // УВАГА: Кодування Windows-1251 є застарілим. Для сучасних програм краще використовувати UTF-8.
    // Якщо ви компілюєте на Linux/macOS, ці рядки потрібно закоментувати.
    SetConsoleOutputCP(1251);
    SetConsoleCP(1251);

    // Отримання коду символу.
    // Шляхом приведення типу 'char' до 'int' ми отримуємо числове значення символу
    // відповідно до його кодування (зазвичай ASCII або розширене ASCII).
    cout << "Код символу 'B': " << (int)'B' << '\n';   // Виведе ASCII-код символу 'B'
    cout << "Код символу '+': " << (int)'+' << '\n';   // Виведе ASCII-код символу '+'
    cout << "Код символу '?': " << (int)'?' << '\n';   // Виведе ASCII-код символу '?'
    
    return 0; // Успішне завершення програми
}
